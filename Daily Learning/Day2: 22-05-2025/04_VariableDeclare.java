// A simple java program to declare variables and assign values later.

public class VariableDeclare {
    public static void main(String[] args) {
        String alienSpecies;
        int eggsFound;
        double averageEggMass;

        alienSpecies = "Zorgon";
        eggsFound = 42;
        averageEggMass = 3.14;

        System.out.println("BEGIN TRANSMISSION");
        System.out.println("Non-Intelligent life form found!");
        System.out.println("\n\nDATA LOADING...");
        System.out.println("\n\nSpeices name: " + alienSpecies);
        System.out.println("No. of Eggs found: " + eggsFound);
        System.out.println("Average egg Mass: " + averageEggMass);
        System.out.println("\n\nENDING TRANSMISSION");
    }
}
